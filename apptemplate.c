void makepalette(int offset) {
    unsigned int i, d;
    int r, g, b;
    for (i = 0; i != 256; i++) {
        d = i + offset;
        r = sinlut[d & 0xff];
        r = (r * r * i) >> 15;
        g = sinlut[(d + 85) & 0xff];
        g = (g * g * i) >> 15;
        b = sinlut[(d + 170) & 0xff];
        b = (b * b * i) >> 15;
        palette[i] = rgbto16(min(r, 255), min(g, 255), min(b, 255));
    }
    palette[0] = 0;
    palette[255] = 0xffff;
}

